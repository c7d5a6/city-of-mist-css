import path from "node:path";
import { fileURLToPath } from "node:url";
import * as esbuild from "esbuild";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

const inputPath = path.join(rootDir, "src", "main.js");
const outputPath = path.join(rootDir, "scripts", "main.js");

async function build() {
  await esbuild.build({
    entryPoints: [inputPath],
    bundle: true,
    format: "esm",
    platform: "browser",
    target: "es2022",
    outfile: outputPath,
    banner: {
      js: "// Auto-generated by npm run build. Edit src/main.js instead.",
    },
  });
  console.log(`Built ${path.relative(rootDir, outputPath)} from src/main.js`);
}

build().catch((error) => {
  console.error("Build failed:", error);
  process.exitCode = 1;
});
